<!-- LES CUISSONS AU FOUR -->

<div class="max-w-5xl mx-auto p-8 bg-white">
  <h2 class="text-3xl font-bold text-center text-purple-600">LES CUISSONS AU FOUR</h2>
  <div class="grid grid-cols-1 md:grid-cols-2 gap-8 mt-8">
    <div>
      <h3 class="text-2xl font-semibold">Recette : Pommes de terre boulangères</h3>
      <p class="my-4">Matériel : Plats en terre cuite, en Pyrex, anti-adhésifs, lèchefrite, Romertöpf, broche, brochettes et papier d'aluminium.</p>
      <p>Ingrédients :</p>
      <ul class="list-disc pl-8">
        <li>800 g de pommes de terre épluchées</li>
        <li>2 gros oignons émincés</li>
        <li>sel, poivre</li>
        <li>10 g de beurre</li>
        <li>eau ou bouillon</li>
      </ul>
      <p>Instructions :</p>
      <ol class="list-decimal pl-8">
        <li>Couper les pommes de terre épluchées en rondelles.</li>
        <li>Disposer dans un plat en terre légèrement beurré, alterner les couches de pommes de terre et d'oignons, saler, poivrer.</li>
        <li>Verser de l'eau ou du bouillon à mi-hauteur.</li>
        <li>Laisser cuire à four moyen (thermostat 6 ou 200°C) pendant 40 minutes.</li>
      </ol>
    </div>
