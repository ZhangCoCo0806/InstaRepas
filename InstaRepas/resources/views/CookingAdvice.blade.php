      <p class="my-4">Matériel : Poêle ou sauteuse anti-adhésives</p>
      <p>Ingrédients :</p>
      <ul class="list-disc pl-8">
        <li>4 escalopes de veau de 150 g</li>
        <li>1 oignon</li>
        <li>1 échalote</li>
        <li>400 g de carottes cuites à la vapeur (ou en boîte)</li>
        <li>sel, poivre</li>
        <li>2 cuillères à soupe de crème fraîche (60g)</li>
      </ul>
      <p>Instructions :</p>
      <ol class="list-decimal pl-8">
        <li>Dans une poêle anti-adhésive, faire dorer les escalopes, réserver.</li>
        <li>Puis faire roussir l'oignon émincé avec un peu d'eau.</li>
        <li>Ajouter la viande, les carottes, saler et poivrer.</li>
        <li>Couvrir et laisser mijoter 5 à 10 mn.</li>
        <li>Juste avant de servir, mettre la crème fraîche, chauffer doucement, servir aussitôt.</li>
