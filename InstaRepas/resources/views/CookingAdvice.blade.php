<div class="max-w-5xl mx-auto p-8">
  <h2 class="text-3xl font-bold text-center text-purple-600">LES CUISSONS A LA POELE</h2>
  <div class="grid grid-cols-1 md:grid-cols-2 gap-8 mt-8">
    <div>
      <h3 class="text-2xl font-semibold">Recette : Filet de merlan à la tomate</h3>
      <p class="my-4">Matériel : Poêle ou sauteuse anti-adhésives</p>
      <p>Ingrédients :</p>
      <ul class="list-disc pl-8">
        <li>4 filets de merlan de 200 g chacun</li>
        <li>1 boîte 4/4 de tomates concassées (ou 2 briques de coulis)</li>
        <li>1 bel oignon émincé</li>
        <li>1 jus de citron + 1 citron</li>
        <li>thym, laurier, sel, poivre</li>
        <li>une branche de persil frisé</li>
      </ul>
      <p>Instructions :</p>
      <ol class="list-decimal pl-8">
        <li>Dans une poêle anti-adhésive, faire revenir l'oignon à sec en versant un peu de jus de citron afin qu'il ne brûle pas.</li>
        <li>Dès la coloration souhaitée, verser le coulis de tomate, le thym et la feuille de laurier, laisser chauffer jusqu'au premier bouillon.</li>
        <li>Disposer les filets de merlan dans le mélange, saler, poivrer.</li>
        <li>Couvrir et laisser cuire 15 mn.</li>
        <li>Décorer avant de servir avec une rondelle de citron et du persil.</li>
      </ol>
    </div>
