      <p class="my-4">Matériel : Plats en terre cuite, en Pyrex, anti-adhésifs, lèchefrite, Romertöpf, broche, brochettes et papier d'aluminium.</p>
      <p>Ingrédients :</p>
      <ul class="list-disc pl-8">
        <li>800 g de poireaux cuits à la vapeur</li>
        <li>Pour la béchamel :</li>
        <li>25 cl de lait 1/2 écrémé</li>
        <li>15 à 20 g de maïzena</li>
        <li>noix de muscade en poudre, sel, poivre</li>
        <li>100 g de gruyère râpé</li>
      </ul>
      <p>Instructions :</p>
      <ol class="list-decimal pl-8">
        <li>Mélanger à froid la maïzena et le lait 1/2 écrémé, porter à ébullition en remuant constamment jusqu'à épaississement du mélange.</li>
        <li>Saupoudrer de noix de muscade, ajouter le sel, le poivre et la moitié du fromage râpé.</li>
        <li>Dans un plat, disposer les poireaux, verser la béchamel puis saupoudrer de fromage râpé.</li>
        <li>Mettre au grill du four pendant 10 à 15 minutes.</li>
      </ol>
    </div>
