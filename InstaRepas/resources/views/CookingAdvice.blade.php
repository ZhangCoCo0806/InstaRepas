<div class="max-w-5xl mx-auto p-8 bg-white">
  <h2 class="text-3xl font-bold text-center text-purple-600">LES PLATS COMPLETS</h2>
  
  <div class="grid grid-cols-1 md:grid-cols-2 gap-8 mt-8">
    <div>
      <h3 class="text-2xl font-semibold">Recette : Quiche "Sans Fond"</h3>
      <p class="my-4">Garniture:</p>
      <ul class="list-disc pl-8">
        <li>4 œufs</li>
        <li>400 ml de lait 1/2 écrémé</li>
        <li>200 g de jambon</li>
        <li>50 g de gruyère râpé</li>
        <li>20 g de maïzena</li>
      </ul>
      <p>Instructions :</p>
      <ol class="list-decimal pl-8">
        <li>Couper le jambon en cubes ou en lamelles.</li>
        <li>Faire tiédir une partie du lait. Le reste sera délayé à froid avec la maïzena, ajouter le lait froid puis doucement le lait tiédi.</li>
        <li>Dans un moule, disposer le jambon, verser le mélange, saupoudrer de gruyère râpé, poivrer.</li>
        <li>Laisser cuire pendant 25 minutes à 180°C.</li>
      </ol>
      <p>À consommer avec une belle portion de crudités en salade.</p>
      <p>NB : peut se réaliser avec 200 g de moules + 200 g de crevettes à la place du jambon + gruyère.</p>
    </div>
    
